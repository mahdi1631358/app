    fun searchErrorCodes(query: String, brand: String, category: String): List<ErrorCode> {
        return staticErrorCodes.filter { error ->
            val matchQuery = query.isEmpty() ||
                    error.code.contains(query, ignoreCase = true) ||
                    error.description.contains(query, ignoreCase = true) ||
                    error.solution.contains(query, ignoreCase = true)

            val matchBrand = brand == "همه برندها" || error.brand == brand
            val matchCategory = category == "همه دستگاه‌ها" || error.category == category

            matchQuery && matchBrand && matchCategory
